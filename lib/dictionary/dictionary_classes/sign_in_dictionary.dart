class SignInDictionary {
  final String signIn;
  final String email;
  final String password;
  final String show;
  final String signInWith;
  final String logIn;
  final String forgotYourPassword;
  final String valueCantBeEmpty;

  const SignInDictionary({
    required this.email,
    required this.password,
    required this.signInWith,
    required this.show,
    required this.signIn,
    required this.forgotYourPassword,
    required this.logIn,
    required this.valueCantBeEmpty,
  });
}
